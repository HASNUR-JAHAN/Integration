                     <h1>FINTECH - in the context of Bangladesh</h1>
                     <h4><p>By the end of 2020, at least 20 billion devices along with more than 5 billion people and businesses will be integrated through digital technology. Digital Financial Service (DFS) promises extensive development of new potential which should lead to an impactful competitive advantage - one of which would be the convergence of several low and middle income individuals along with SMEs into a financial inclusion domain. Digitising transaction and exchange coupled with financial products is anticipated to further boost financial inclusion in the coming days.</p>

<p>Most developing countries have myriads of small businesses that are not united under a formal financial system. DFS can address and facilitate rectification of such a problem through a digital wallet which will remove the barrier of current entry through the promotion of all forms of transactions, payments and financing i.e., loans, as well as allowing access to internet commerce. Market started off as barter exchange economy, then intermediary, state run currency and then it evolved to finally reach the modern crypto currency. Dynamics still persists for further evolution that may prompt full proof and matured e-KYC, integrating devices to lifestyles of people, virtual tokens and many more. Furthermore, MFS/MAFS (Multiple access financial service) have undergone development to being more than just a transaction channel to a comprehensive financial solution platform.</p>

<p>Now it may cross the minds of people that since 2005 several initiatives have failed to launch MFS. And we may wonder why? The reasons are not unknown at all. Some of these initiatives only facilitated merchant payment, some only used a certain structure for account opening, some only offered limited services, and others are integrated with only bank account etc. On the other hand, under a global view several businesses changed and owned the MFS game and redefined the limits of the medium itself. Such names are M-PESA which drove customers to have mobile financial services under their USSD and it was supported by the Kenyan government who partially owned this company, and this initiative is telco-led structure. Further with its sister service M-SHWARI, it allowed saving and loans to occur through digitised mediums.  Soon, financial inclusion among Kenyans boomed from 27 per cent in 2006 to 75 per cent in 2016. The Indians drove such growth using Paytm not through Cash-in Cash-out root though and the Chinese contestants introduced further advancement in services through WeChat pay and AliPay.</p>

<p>Taking a look at Bangladesh, in order to boost financial inclusion, Bangladesh Bank issued a draft guideline (2011) and granted permission to 28 banks to provide mobile finance under a bank-led structure. As of June 2017, 18 banks had already launched such services. Among the contestants, bKash has evidently been the leader. From all the different services it provides, a prime slice of the scenario includes 'send money', 'cash in' and 'cash out'. bKash became the world's largest mobile financial service provider in terms of the number of subscribers, agents and transactions within 5 years since launch. This may be due to its out-of-the-box business model, vigorous distribution network with innovative and efficient structure, effective cash management and the truculent customer acquisition drive. Furthermore, the accelerator of growth was also reinforced by its unique company structure. There are several others following the same format which has created a monopolised market and an unfriendly environment for any competition in Bangladesh. Competition is extremely critical for the overall development of the market and versatility of products to be offered for the customers.</p>

<p>In order to build a successful MFS/ MAFS provider, the following flowchart has to be followed:</p>

<p>As of 2018, Bangladesh had already achieved a stunning 37 per cent financial inclusion from 16 per cent in 2011. The low cost, easy to access and convenient nature of MFS/ MAFS has significantly contributed to its inclusion to the country's unbanked population. That is because it can reach places and people far better than banks.</p>

<p>To be more mature, successful and to reduce the number of unbanked population, we have to focus on Telco, bank, internet and handset industries simultaneously. All industries have to come forward to make the eco system complete.</p>

<p>The unbanked population is like the devil to the growth of financial inclusion. As per World Bank study, 2017, 1/3 of the global population is unbanked with 1 billion in Asia itself. A few reasons to be focused on for people being unbanked are: lower financial literacy, unavailability of bank branches, expensive transaction cost for banks etc. Banks are looking for solution through agent banking but it won't be feasible if it is not strategically merged with MFS/ MAFS.</p>

<p>The ultimate destination for Fintech is to develop a full-fledged DFS ecosystem which should comprise all the elements ranging from consumers to a platform for nationwide delivery infrastructure. In a country such as Bangladesh, implementation of a DFS is set to offer several important benefits. It would ease up the access to formal financial services by signup through eKYC. It is likely to generate an easier loan facility with credit rating after using any app or machine based solutions. Customers/ SME's efficiency and capacity would further increase and produce finest products. The market, both local and global should become more accessible with transactions being made easy through market place.</p>

<p>A few examples of financial products/services include bKash, NAGAD, Rocket, Pathao, Ajkerdeal, Uber, Daraz etc.</p>

<p>Conclusively, DFS is about the expansion and creation of new business ecosystems by connecting the dots between the physical and virtual elements of business through the integration of man and machine with the medium of finance. The development of enhanced data connectivity is facilitating customers or SME's with digital wallets - a virtual source of money, transaction, credit rating and finance - to boost electronic market place. This end to end digitalisation is the example of the fourth industrial revolution, that allows new streams of real time data being converged with accessible finance, exchange, transaction and most importantly, it allows SMEs to grow. The future of FINTECH in the context of Bangladesh is yet to evolve and catch up with the global giants.

